#include "3-calc.h"

/**
 * get_op_func - This file determines the appropriate function to execute
 * @s: The operator passed as argument.
 *
 * Return: A function pointer pointing to the function that corresponds
 */

int (*get_op_func(char *s))(int, int)
{
	op_t ops[] = {
		{"+", op_add},
		{"-", op_sub},
		{"*", op_mul},
		{"/", op_div},
		{"%", op_mod},
		{NULL, NULL}
	};
	int i = 0;

	while (ops[i].op != NULL && *(ops[i].op) != *s)
		i++;

	return (ops[i].f);
}
